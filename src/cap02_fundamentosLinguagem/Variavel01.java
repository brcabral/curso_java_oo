package cap02_fundamentosLinguagem;

public class Variavel01 {
    public static void main(String[] args) {
        // Declarando uma variável de números inteiro
        int quantidade;
        // Atribuindo o valor 10 para a variável
        quantidade = 10;

        System.out.println(quantidade);

        quantidade = 15;
        System.out.println(quantidade);
    }
}